import { join } from 'node:path';
import { writeFile } from 'node:fs/promises';

import type { Configuration } from '../cfg/types';

export function writeMultiPlatform(cfg: Configuration) {
  const { rootPath, isSP } = cfg;

  if (isSP) return;

  const { platform, pageType } = cfg.MPConfiguration;

  writeFile(
    join(rootPath, './helpers/multi-platforms.ts'),
    `// ðŸ“¢ Do not manually modify, automatically generated by ESBoot.\nexport * from '@${platform}-${pageType}/helpers/multi-platforms';\n`
  ).catch((err) => {
    console.error(`generate multi-platforms.ts failed: ${err.message}`);
  });
}
